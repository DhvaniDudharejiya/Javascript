let responseCode = 356

// Compare using a switch
switch (responseCode) {
    case 036: console.log("Australia")
        break
    case 076: console.log("Brazil")
        break
    case 124: console.log("Canada")
        break
    case 300: console.log("Greece")
        break
    case 356: console.log("India")
        break
    default: console.log("Unknown Code")
}
